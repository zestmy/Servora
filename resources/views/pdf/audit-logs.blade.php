                $summary = [];
                foreach ($keys as $k) {
                    $b = array_key_exists($k, $old) ? $scalar($old[$k], $k) : '';
                    $a = array_key_exists($k, $new) ? $scalar($new[$k], $k) : '';
                    $summary[] = $k . ': ' . $b . ' → ' . $a;
                }
