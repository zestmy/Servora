            $scalar = function ($v, $k = '') use ($fkLabels) {
                if (is_null($v)) return '';
                if (is_bool($v)) return $v ? 'true' : 'false';
                if (is_array($v)) return json_encode($v);
                if (is_numeric($v) && isset($fkLabels[$k][(int) $v])) return $fkLabels[$k][(int) $v] . ' (#' . $v . ')';
